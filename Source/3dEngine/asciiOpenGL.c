// draw single character
void drawAsciiChar(int charNum, float xSz, float ySz,
                   float xLoc, float yLoc, float zLoc,
                   float xRot, float yRot, float zRot, float thickness, int color)
{
   int i, numOfPts, numOfCoords, bgnLinePt;
   
   GLfloat *ascii_POINT;

   if ((charNum > 94) || (charNum < 1))
   return;
   
   numOfPts = ascii_LINEDATSZ[charNum] - ascii_LINEDATSZ[charNum-1];
   bgnLinePt = ascii_LINEDATSZ[charNum-1];
   
   ascii_POINT = (GLfloat *)malloc(numOfPts * 3 * sizeof(GLfloat));
   

   // get the coordinates for the wanted letter
   numOfCoords = 0;
   for (i=0; i < numOfPts; i++)
   {
      ascii_POINT[numOfCoords] = ascii_STATICPOINT[ascii_LINE[bgnLinePt+i]*3+0];
      numOfCoords++;
      ascii_POINT[numOfCoords] = ascii_STATICPOINT[ascii_LINE[bgnLinePt+i]*3+1];
      numOfCoords++;
      ascii_POINT[numOfCoords] = ascii_STATICPOINT[ascii_LINE[bgnLinePt+i]*3+2];
      numOfCoords++;
   }
   
   
   // resize
   numOfCoords = 0;
   for (i=0; i < numOfPts; i++)
   {
      ascii_POINT[numOfCoords] *= xSz;//x
      numOfCoords++;
      ascii_POINT[numOfCoords] *= ySz;//y
      numOfCoords++;
      numOfCoords++;
   }
   
   // add location
   numOfCoords = 0;
   for (i=0; i < numOfPts; i++)
   {
      ascii_POINT[numOfCoords] += xLoc;//x
      numOfCoords++;
      ascii_POINT[numOfCoords] += yLoc;//y
      numOfCoords++;
      ascii_POINT[numOfCoords] += zLoc;//z
      numOfCoords++;
   }
   
   
   // apply rotation
   static float xRotStatic = 0, yRotStatic = 0, zRotStatic = 0;
   static float yUc1 = 1.0, zUc1 = 0.0;
   static float xUc2 = 1.0, zUc2 = 0.0;
   static float xUc3 = 1.0, yUc3 = 0.0;
   
   // if new rotations then create new unit circle coordinate data
   if ((xRotStatic != xRot) ||
       (yRotStatic != yRot) ||
       (zRotStatic != zRot))
   {
      xRotStatic = xRot;
      yRotStatic = yRot;
      zRotStatic = zRot;
      
      yUc1 = 1.0;
      zUc1 = 0.0;
      xUc2 = 1.0;
      zUc2 = 0.0;
      xUc3 = 1.0;
      yUc3 = 0.0;
      
      addRotToXyzUcRot(&yUc1, &zUc1,
                       &xUc2, &zUc2,
                       &xUc3, &yUc3, xRotStatic, yRotStatic, zRotStatic);
   }
   
   // apply the rotation to each point using unit circle coordinate data
   for (i=0; i < numOfPts*3; i=i+3)
   applyXyzUcRotToPoint(yUc1, zUc1,
                        xUc2, zUc2,
                        xUc3, yUc3, &ascii_POINT[0+i], &ascii_POINT[1+i], &ascii_POINT[2+i]);
   
   
   
   glLineWidth(thickness);
   
   glEnableClientState(GL_VERTEX_ARRAY);
   glVertexPointer(3, GL_FLOAT, 0, ascii_POINT);
   
   glDisableClientState(GL_COLOR_ARRAY);
   
   // use this color instead of the color array data
   glColor4f((color&0xFF)/255.0, ((color&0xFF00)>>8)/255.0, ((color&0xFF0000)>>16)/255.0, 1.0);

   // draw lines
   glDrawArrays(GL_LINES, 0, numOfPts);
   
   free(ascii_POINT);
}

// draw string
void drawAscii(char *asciiStr, float xSz, float ySz,
               float xLoc, float yLoc, float zLoc,
               float xRot, float yRot, float zRot, float thickness, int color, int antialiasing)
{
   int i;
   float charLoc = 0;
   int ascii_MESHNUM;
   
   if (antialiasing)
   {
      glEnable(GL_LINE_SMOOTH);
      glHint(GL_LINE_SMOOTH_HINT, GL_NICEST);
   }
   
   // draw each character
   for (i=0; i < strlen(asciiStr); i++)
   {
      ascii_MESHNUM = (int)asciiStr[i]-32;
      
      drawAsciiChar(ascii_MESHNUM, xSz, ySz,
                    xLoc+charLoc, yLoc, zLoc,
                    xRot, yRot, zRot, thickness, color);
      
      charLoc += xSz;
   }
   
   if (antialiasing)
   glDisable(GL_LINE_SMOOTH);
}
